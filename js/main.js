let b6toggle = document.querySelector(".button-6 .toggle");

b6toggle.addEventListener("click", () => {
    if(!b6toggle.classList.contains("forward")) {
        b6toggle.classList.add("forward");
        b6toggle.classList.remove("reverse");
    } else {
        b6toggle.classList.add("reverse");
        b6toggle.classList.remove("forward");
    }
});


